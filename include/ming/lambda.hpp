#define LAMBDA_COMMUTATIVE_OPERATOR(OP) \
    template <class T> \
    auto operator OP (ming::lambda::Lambda l, T&& x) { \
        return [&x](T& y) { return y OP std::forward<T>(x); }; \
    } \
    template <class T> \
    auto operator OP (T&& x, ming::lambda::Lambda l) { \
        return l + x; \
    }

